#!/usr/bin/env bun

import assert from "node:assert/strict";
import { createHash } from "node:crypto";
import fs from "node:fs";
import os from "node:os";
import path from "node:path";

const repoRoot = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const configDir = fs.mkdtempSync(path.join(os.tmpdir(), "opencode-notion-assets-test-"));
const normalizer = path.join(repoRoot, "scripts", "normalize-opencode-notion-assets.mjs");
const relativePaths = [
  "commands/mobile-on-call/init.md",
  "commands/mobile-on-call/triage.md",
  "skills/mobile-design-review/SKILL.md",
];
const manifestPath = path.join(
  configDir,
  ".managed",
  "claude-config",
  "notion-assets.json",
);
const pluginGeneratedAgentMarker =
  "<!-- WARNING: This file is generated by `notion ai plugins add --agent opencode`. Update the source plugin agent instead. -->";

function writeAssets(prefix) {
  for (const relativePath of relativePaths) {
    const filePath = path.join(configDir, relativePath);
    fs.mkdirSync(path.dirname(filePath), { recursive: true });
    fs.writeFileSync(filePath, `${prefix}:${relativePath}\n`);
  }
}

function run(...arguments_) {
  const result = Bun.spawnSync(["bun", normalizer, configDir, ...arguments_], {
    stdout: "pipe",
    stderr: "pipe",
  });
  assert.equal(result.exitCode, 0, result.stderr.toString());
}

function sha256(content) {
  return createHash("sha256").update(content).digest("hex");
}

try {
  const unsupportedAgentPath = path.join(
    configDir,
    "agents",
    "periphery-fixer.md",
  );
  fs.mkdirSync(path.dirname(unsupportedAgentPath), { recursive: true });
  fs.writeFileSync(
    unsupportedAgentPath,
    `${pluginGeneratedAgentMarker}\nunsupported agent fixture\n`,
  );
  run("--retire-unsupported-agents");
  assert.equal(fs.existsSync(unsupportedAgentPath), false);
  const unsupportedRetirementRoot = path.join(
    configDir,
    "backups",
    "setup-opencode",
  );
  const unsupportedRetirementDirectories = fs.readdirSync(unsupportedRetirementRoot)
    .filter((name) => name.startsWith("retired-unsupported-agents."));
  assert.equal(unsupportedRetirementDirectories.length, 1);
  assert.equal(
    fs.readFileSync(
      path.join(
        unsupportedRetirementRoot,
        unsupportedRetirementDirectories[0],
        "agents",
        "periphery-fixer.md",
      ),
      "utf8",
    ),
    `${pluginGeneratedAgentMarker}\nunsupported agent fixture\n`,
  );

  fs.writeFileSync(unsupportedAgentPath, "user-authored agent fixture\n");
  run("--retire-unsupported-agents");
  assert.equal(
    fs.readFileSync(unsupportedAgentPath, "utf8"),
    "user-authored agent fixture\n",
  );
  assert.equal(
    fs.readdirSync(unsupportedRetirementRoot)
      .filter((name) => name.startsWith("retired-unsupported-agents.")).length,
    1,
  );

  const retiredPaths = [
    "plugins/advisor.ts",
    "plugins/advisor.config.json",
    "plugins/advisor.config.local.json",
  ];
  for (const relativePath of retiredPaths) {
    const filePath = path.join(configDir, relativePath);
    fs.mkdirSync(path.dirname(filePath), { recursive: true });
    fs.writeFileSync(filePath, `retired:${relativePath}\n`);
  }
  fs.mkdirSync(path.dirname(manifestPath), { recursive: true });
  fs.writeFileSync(manifestPath, JSON.stringify({
    schema: 1,
    assets: {
      sentinel: { sha256: "kept" },
    },
  }));
  fs.writeFileSync(path.join(configDir, "package.json"), JSON.stringify({
    dependencies: { "@opencode-ai/plugin": "1.17.20" },
  }));
  fs.writeFileSync(path.join(configDir, "package-lock.json"), "retired lock\n");
  fs.mkdirSync(path.join(configDir, "node_modules"));
  fs.writeFileSync(path.join(configDir, "node_modules", "sentinel"), "retired module\n");
  run("--retire-obsolete");
  for (const relativePath of retiredPaths) {
    assert.equal(fs.existsSync(path.join(configDir, relativePath)), false);
  }
  const retirementRoot = path.join(configDir, "backups", "setup-opencode");
  const retirementDirectories = fs.readdirSync(retirementRoot)
    .filter((name) => name.startsWith("retired-advisor."));
  assert.equal(retirementDirectories.length, 1);
  for (const relativePath of retiredPaths) {
    assert.equal(
      fs.readFileSync(
        path.join(retirementRoot, retirementDirectories[0], relativePath),
        "utf8",
      ),
      `retired:${relativePath}\n`,
    );
  }
  assert.deepEqual(
    JSON.parse(fs.readFileSync(path.join(configDir, "package.json"), "utf8")),
    {},
  );
  assert.equal(fs.existsSync(path.join(configDir, "package-lock.json")), false);
  assert.equal(fs.existsSync(path.join(configDir, "node_modules")), false);
  assert.equal(
    fs.readFileSync(
      path.join(retirementRoot, retirementDirectories[0], "package-lock.json"),
      "utf8",
    ),
    "retired lock\n",
  );
  assert.equal(
    fs.readFileSync(
      path.join(
        retirementRoot,
        retirementDirectories[0],
        "node_modules",
        "sentinel",
      ),
      "utf8",
    ),
    "retired module\n",
  );
  const retiredManifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
  assert.equal(retiredManifest.assets["plugins/advisor.ts"], undefined);
  assert.deepEqual(retiredManifest.assets.sentinel, { sha256: "kept" });

  writeAssets("baseline");
  const originalManifest = '{"schema":1,"assets":{"sentinel":{"sha256":"kept"}}}\n';
  fs.writeFileSync(manifestPath, originalManifest);
  const original = Object.fromEntries(relativePaths.map((relativePath) => [
    relativePath,
    fs.readFileSync(path.join(configDir, relativePath), "utf8"),
  ]));

  run("--prepare-refresh");
  writeAssets("unvalidated-refresh");
  run("--restore-refresh");
  for (const relativePath of relativePaths) {
    assert.equal(
      fs.readFileSync(path.join(configDir, relativePath), "utf8"),
      original[relativePath],
    );
  }
  assert.equal(fs.readFileSync(manifestPath, "utf8"), originalManifest);
  assert.equal(
    fs.existsSync(path.join(configDir, ".managed", "claude-config", "refresh-pending")),
    false,
  );

  const reviewSkillPath = path.join(
    configDir,
    "skills",
    "mobile-design-review",
    "SKILL.md",
  );
  fs.rmSync(reviewSkillPath);
  run("--prepare-refresh");
  fs.writeFileSync(reviewSkillPath, "new unvalidated review skill\n");
  run("--restore-refresh");
  assert.equal(fs.existsSync(reviewSkillPath), false);
  assert.equal(fs.readFileSync(manifestPath, "utf8"), originalManifest);

  fs.writeFileSync(reviewSkillPath, "baseline:skills/mobile-design-review/SKILL.md\n");
  run("--prepare-refresh");
  writeAssets("validated-refresh");
  fs.writeFileSync(
    unsupportedAgentPath,
    `${pluginGeneratedAgentMarker}\ngenerated during refresh\n`,
  );
  run("--retire-unsupported-agents");
  assert.equal(fs.existsSync(unsupportedAgentPath), false);
  run("--commit-refresh");

  const postRefreshRetirements = fs.readdirSync(unsupportedRetirementRoot)
    .filter((name) => name.startsWith("retired-unsupported-agents."));
  assert.equal(postRefreshRetirements.length, 2);
  assert.ok(postRefreshRetirements.some((directory) =>
    fs.readFileSync(
      path.join(
        unsupportedRetirementRoot,
        directory,
        "agents",
        "periphery-fixer.md",
      ),
      "utf8",
    ) === `${pluginGeneratedAgentMarker}\ngenerated during refresh\n`
  ));

  const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
  for (const relativePath of relativePaths) {
    const content = fs.readFileSync(path.join(configDir, relativePath));
    assert.equal(manifest.assets[relativePath].sha256, sha256(content));
  }
  assert.equal(
    fs.existsSync(path.join(configDir, ".managed", "claude-config", "refresh-pending")),
    false,
  );

  fs.writeFileSync(
    reviewSkillPath,
    "2. **Reference (if Figma URL)** — before any `mcp__plugin_figma_figma__*` call:\n   1. Verify the Figma MCP is installed: `claude mcp list 2>/dev/null | grep -i figma`. If no match, install it: `claude mcp add figma-mcp --transport sse https://mcp.figma.com/sse`. Tell the user the MCP was just installed and that they may need to restart the Claude Code session for the tools to register; if so, pause and ask them to restart before proceeding.\n   2. Load the `figma:figma-use` skill — it is a MANDATORY prerequisite for every `use_figma` call.\n   3. Call `get_design_context` and `get_variable_defs` on the target node to extract spec values.\nusing the connected Figma MCP, pull `get_design_context` + `get_variable_defs`\nusing the connected Figma MCP, also pull `get_design_context` + `get_variable_defs`\n- **Figma MCP** when using a Figma URL. If not installed, the workflow's first step installs it via `opencode mcp add figma-mcp --url https://mcp.figma.com/sse`. Authenticate with `opencode mcp auth figma-mcp` if the server requires it.\n",
  );
  fs.writeFileSync(
    path.join(configDir, "commands", "mobile-on-call", "init.md"),
    "notion-dev_\nopencode mcp auth notion-dev\nOffer to authenticate it by running:\nThe read/write Notion MCP is installed at the user level; this authorizes the current OpenCode profile.\nAfter authenticating, re-run the check\n",
  );
  fs.writeFileSync(
    path.join(configDir, "commands", "mobile-on-call", "triage.md"),
    "notion-dev_\n",
  );
  run();
  const normalizedReviewSkill = fs.readFileSync(reviewSkillPath, "utf8");
  assert.match(normalizedReviewSkill, /`opencode mcp list 2>\/dev\/null \| rg -i figma`/);
  assert.doesNotMatch(normalizedReviewSkill, /claude mcp list/);

  console.log("OK     OpenCode Notion asset refresh transaction");
} finally {
  fs.rmSync(configDir, { recursive: true, force: true });
}
